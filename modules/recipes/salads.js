// Для удобства работы с пропущенными ингредиентами
export const getRecipesByIngredients = (userIngredients) => {
  const perfectMatches = [];
  const recommended = [];

  recipesDB.forEach(recipe => {
    const missing = recipe.ingredients.filter(ing => !userIngredients.includes(ing));
    if (missing.length === 0) {
      perfectMatches.push(recipe);
    } else if (missing.length <= 3) {
      recommended.push({ ...recipe, missingCount: missing.length, missingList: missing });
    }
  });

  return { perfectMatches, recommended };
};

export default recipesDB;
