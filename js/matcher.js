// Нормализация ингредиента
export function normalizeIngredient(name) {
    if (!name) return '';
    
    let normalized = name.toLowerCase().trim();
    
    // Убираем всё в скобках
    normalized = normalized.replace(/[\(（][^\)）]*[\)）]/g, '');
    
    // Убираем тире и всё после
    normalized = normalized.replace(/\s*[-—–]\s*.*$/, '');
    
    // Убираем цифры и дроби в начале
    normalized = normalized.replace(/^[\d\s\/\+\-\(\)]+/, '');
    
    // Убираем слова-маркеры
    normalized = normalized.replace(/^(около|примерно|~|по\s*вкусу|до|несколько|половина|четверть|щепотка|щепотку|пару|пара)\s*/i, '');
    
    // Убираем единицы измерения
    normalized = normalized.replace(/\s*(г|гр|грамм|грамма|кг|мл|л|шт|штук|штуки|банка|банки|пучок|пучка|зубчик|зубчика|ст\.?\s*ложк[аи]|ложк[аи]|ст\.?\s*л|ч\.?\s*ложк[аи]|ч\.?\s*л|стакан[а]?|стак\.|капля|капель|щепотк[ау])\b.*$/i, '');
    normalized = normalized.replace(/\s*(столовая|чайная|десертная)\s*ложка\b.*$/i, '');
    
    // Убираем прилагательные и определения
    const removeWords = [
        'свежий', 'свежие', 'свежая', 'свежее', 'свежих',
        'соленый', 'соленые', 'соленая', 'солёный', 'солёные', 'солёная',
        'маринованный', 'маринованные', 'маринованная',
        'вареный', 'вареная', 'варёный', 'варёная',
        'копченый', 'копченая', 'копчёный', 'копчёная',
        'слабосоленый', 'слабосолёный', 'слабосолёная',
        'сушеный', 'сушеные', 'вяленый', 'вяленые',
        'консервированный', 'консервированная', 'консервированные',
        'замороженный', 'замороженные', 'замороженная',
        'кипяченый', 'кипячёный', 'отварной', 'печёный', 'запечёный',
        'тёртый', 'тертый', 'нарезанный', 'измельченный',
        'жидкий', 'густой', 'простой', 'готовый',
        'мелкий', 'крупный', 'средний',
        'домашний', 'домашние',
        'натуральный', 'натуральные',
        'целый', 'целые', 'целая',
        'очищенный', 'очищенные',
        'натертый', 'натертые',
        'молотый', 'молотая', 'молотое', 'молотые'
    ];
    for (let word of removeWords) {
        normalized = normalized.replace(new RegExp(`\\b${word}\\s*`, 'gi'), '');
    }
    
    // Убираем "по вкусу", "по желанию", "по необходимости"
    normalized = normalized.replace(/\s*по\s*(вкусу|желанию|необходимости)\s*/gi, '');
    
    // Убираем лишние пробелы
    normalized = normalized.trim();
    
    // Специальные преобразования для яиц (до сортировки слов)
    // "яйцо куриное" → "яйца куриные"
    if (normalized === 'яйцо куриное' || normalized === 'куриное яйцо') {
        normalized = 'яйца куриные';
    }
    
    // Преобразование числа (единственное ↔ множественное)
    const numberTransform = {
        // Яйца (особый случай)
        'яйцо': 'яйца куриные',
        'яйца': 'яйца куриные',
        'яйцо куриное': 'яйца куриные',
        'куриное яйцо': 'яйца куриные',
        'яйца куриные': 'яйца куриные',
        'яйцо перепелиное': 'яйца перепелиные',
        'яйца перепелиные': 'яйца перепелиные',
        
        // Овощи
        'кабачок': 'кабачки', 'баклажан': 'баклажаны', 'помидор': 'помидоры',
        'огурец': 'огурцы', 'редис': 'редиски',
        'кабачки': 'кабачки', 'баклажаны': 'баклажаны', 'помидоры': 'помидоры',
        'огурцы': 'огурцы',
        
        // Фрукты
        'яблоко': 'яблоки', 'груша': 'груши', 'банан': 'бананы',
        'апельсин': 'апельсины', 'лимон': 'лимоны', 'мандарин': 'мандарины',
        'киви': 'киви', 'персик': 'персики', 'абрикос': 'абрикосы',
        'яблоки': 'яблоки', 'груши': 'груши', 'бананы': 'бананы',
        'апельсины': 'апельсины', 'лимоны': 'лимоны', 'мандарины': 'мандарины',
        'персики': 'персики', 'абрикосы': 'абрикосы',
        
        // Грибы
        'гриб': 'грибы', 'шампиньон': 'шампиньоны',
        'грибы': 'грибы', 'шампиньоны': 'шампиньоны',
        
        // Мясо
        'сосиска': 'сосиски', 'сарделька': 'сардельки',
        'сосиски': 'сосиски', 'сардельки': 'сардельки',
        
        // Специи (неизменяемы)
        'соль': 'соль', 'перец': 'перец',
        'укроп': 'укроп', 'петрушка': 'петрушка', 'кинза': 'кинза',
        'базилик': 'базилик', 'мята': 'мята'
    };
    
    // Применяем преобразование числа
    if (numberTransform[normalized]) {
        normalized = numberTransform[normalized];
    }
    
    // Сортируем слова для сравнения
    const words = normalized.split(/\s+/);
    if (words.length > 1) {
        words.sort();
        normalized = words.join(' ');
    }
    
    return normalized;
}

// Сравнение ингредиентов
export function isIngredientMatch(userIngredient, recipeIngredient) {
    if (!userIngredient || !recipeIngredient) return false;
    
    const userNorm = normalizeIngredient(userIngredient);
    const recipeNorm = normalizeIngredient(recipeIngredient);
    
    if (userNorm === recipeNorm) return true;
    if (userNorm.includes(recipeNorm) || recipeNorm.includes(userNorm)) return true;
    
    // ========== РАСШИРЕННЫЕ СИНОНИМЫ ==========
    const synonyms = {
        // Яйца
        'яйца куриные': ['яйцо', 'яйца', 'яйцо куриное', 'куриное яйцо', 'яйца куриные', 'куриные яйца'],
        'яйца': ['яйцо', 'яйца куриные', 'яйцо куриное', 'куриное яйцо', 'куриные яйца'],
        
        // Соль
        'соль': ['соль поваренная', 'соль морская', 'соли', 'поваренная соль', 'морская соль', 'каменная соль', 'йодированная соль'],
        'соль поваренная': ['соль', 'соль морская', 'соли', 'поваренная соль'],
        'соль морская': ['соль', 'соль поваренная', 'соли', 'морская соль'],
        
        // Перец
        'перец': ['перец черный', 'черный перец', 'перец черный молотый', 'молотый перец', 'перец душистый', 'перец красный', 'красный перец', 'перец чили', 'перец белый', 'белый перец', 'смесь перцев'],
        'перец черный': ['перец', 'черный перец', 'перец черный молотый', 'молотый перец', 'смесь перцев'],
        'перец черный молотый': ['перец', 'черный перец', 'перец черный', 'молотый перец'],
        'перец красный жгучий': ['перец', 'красный перец', 'перец чили', 'жгучий перец'],
        
        // Овощи
        'кабачки': ['кабачок', 'цукини'],
        'баклажаны': ['баклажан', 'синенькие'],
        'помидоры': ['помидор', 'томат', 'томаты'],
        'огурцы': ['огурец', 'огурчик', 'корнишоны'],
        
        // Грибы
        'грибы': ['гриб', 'шампиньоны', 'белые грибы', 'лисички', 'маслята'],
        
        // Мясо
        'курица': ['куриное филе', 'куриная грудка', 'курицы', 'филе курицы'],
        
        // Масло
        'масло': ['масло подсолнечное', 'масло растительное', 'подсолнечное масло', 'растительное масло'],
        'масло растительное': ['масло подсолнечное', 'подсолнечное масло', 'масло'],
        
        // Лук и чеснок
        'лук': ['лук репчатый', 'репчатый лук', 'луковица'],
        'чеснок': ['зубчик чеснока', 'чеснока зубчик', 'чеснок зубчиками']
    };
    
    // Проверка синонимов для userNorm
    if (synonyms[userNorm]) {
        for (let syn of synonyms[userNorm]) {
            if (syn === recipeNorm || recipeNorm.includes(syn) || syn.includes(recipeNorm)) {
                return true;
            }
        }
    }
    
    // Проверка синонимов для recipeNorm
    if (synonyms[recipeNorm]) {
        for (let syn of synonyms[recipeNorm]) {
            if (syn === userNorm || userNorm.includes(syn) || syn.includes(userNorm)) {
                return true;
            }
        }
    }
    
    return false;
}
