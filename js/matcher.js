// Нормализация ингредиента
export function normalizeIngredient(name) {
    let normalized = name.toLowerCase().trim();
    
    // Убираем всё в скобках
    normalized = normalized.replace(/[\(（][^\)）]*[\)）]/g, '');
    
    // Убираем тире и всё после
    normalized = normalized.replace(/\s*[-—–]\s*.*$/, '');
    
    // Убираем цифры и дроби в начале
    normalized = normalized.replace(/^[\d\s\/\+\-\(\)]+/, '');
    
    // Убираем слова-маркеры
    normalized = normalized.replace(/^(около|примерно|~|по\s*вкусу|до|несколько|половина|четверть|щепотка|щепотку)\s*/i, '');
    
    // Убираем единицы измерения
    normalized = normalized.replace(/\s*(г|гр|грамм|грамма|кг|мл|л|шт|штук|штуки|банка|банки|пучок|пучка|зубчик|зубчика|ст\.?\s*ложк[аи]|ложк[аи]|ст\.?\s*л|ч\.?\s*ложк[аи]|ч\.?\s*л|стакан[а]?|стак\.|капля|капель)\b.*$/i, '');
    normalized = normalized.replace(/\s*(столовая|чайная|десертная)\s*ложка\b.*$/i, '');
    
    // Убираем прилагательные и определения
    const removeWords = [
        'свежий', 'свежие', 'свежая', 'свежее', 'свежих',
        'соленый', 'соленые', 'соленая', 'солёный', 'солёные', 'солёная',
        'маринованный', 'маринованные', 'маринованная',
        'вареный', 'вареная', 'варёный', 'варёная',
        'копченый', 'копченая', 'копчёный', 'копчёная',
        'слабосоленый', 'слабосолёный', 'слабосолёная',
        'сушеный', 'сушеные', 'вяленый', 'вяленые',
        'консервированный', 'консервированная', 'консервированные',
        'замороженный', 'замороженные', 'замороженная',
        'кипяченый', 'кипячёный', 'отварной', 'печёный', 'запечёный',
        'тёртый', 'тертый', 'нарезанный', 'измельченный',
        'жидкий', 'густой', 'простой', 'готовый',
        'мелкий', 'крупный', 'средний',
        'домашний', 'домашние',
        'натуральный', 'натуральные'
    ];
    for (let word of removeWords) {
        normalized = normalized.replace(new RegExp(`\\b${word}\\s*`, 'gi'), '');
    }
    
    // Убираем "по вкусу", "по желанию", "по необходимости"
    normalized = normalized.replace(/\s*по\s*(вкусу|желанию|необходимости)\s*/gi, '');
    
    // Убираем "молотый" после перца
    normalized = normalized.replace(/перец\s+черный\s+молотый/gi, 'перец черный');
    normalized = normalized.replace(/перец\s+черный/gi, 'перец черный');
    
    // Убираем "поваренная" после соли
    normalized = normalized.replace(/соль\s+поваренная/gi, 'соль');
    
    // Убираем лишние пробелы
    normalized = normalized.trim();
    
    // Приведение к базовой форме (единственное число, именительный падеж)
    const singularForms = {
        'яйца': 'яйцо', 'яиц': 'яйцо', 'яйцу': 'яйцо', 'яйцом': 'яйцо',
        'огурцы': 'огурец', 'огурцов': 'огурец', 'огурцам': 'огурец', 'огурцами': 'огурец',
        'помидоры': 'помидор', 'помидоров': 'помидор', 'помидорам': 'помидор',
        'картофель': 'картофель',
        'морковь': 'морковь',
        'лук': 'лук', 'лука': 'лук', 'луку': 'лук', 'луком': 'лук',
        'сосиски': 'сосиска', 'сосисок': 'сосиска', 'сосискам': 'сосиска',
        'яблоки': 'яблоко', 'яблок': 'яблоко', 'яблокам': 'яблоко',
        'груши': 'груша', 'груш': 'груша', 'грушам': 'груша',
        'бананы': 'банан', 'бананов': 'банан', 'бананам': 'банан',
        'апельсины': 'апельсин', 'апельсинов': 'апельсин',
        'лимоны': 'лимон', 'лимонов': 'лимон',
        'сыры': 'сыр', 'сыров': 'сыр',
        'грибы': 'гриб', 'грибов': 'гриб',
        'орехи': 'орех', 'орехов': 'орех',
        'листья': 'лист', 'листьев': 'лист',
        'веточки': 'веточка', 'веточек': 'веточка'
    };
    
    if (singularForms[normalized]) {
        normalized = singularForms[normalized];
    }
    
    // Сортируем слова для сравнения (семечки тыквенные = тыквенные семечки)
    const words = normalized.split(/\s+/);
    if (words.length > 1) {
        words.sort();
        normalized = words.join(' ');
    }
    
    return normalized;
}

// Сравнение ингредиентов
export function isIngredientMatch(userIngredient, recipeIngredient) {
    const userNorm = normalizeIngredient(userIngredient);
    const recipeNorm = normalizeIngredient(recipeIngredient);
    
    if (userNorm === recipeNorm) return true;
    
    // Проверка вхождения
    if (userNorm.includes(recipeNorm) || recipeNorm.includes(userNorm)) return true;
    
    // Специальные синонимы
    const synonyms = {
        'соль': ['соль поваренная', 'соль морская', 'соли'],
        'перец черный': ['перец', 'черный перец', 'перец чёрный', 'перца'],
        'яйцо': ['яйца', 'куриное яйцо', 'яиц'],
        'яйца': ['яйцо', 'куриное яйцо'],
        'курица': ['куриное филе', 'куриная грудка', 'курицы'],
        'огурцы': ['огурец', 'огурцов', 'огурца'],
        'огурец': ['огурцы', 'огурцов', 'огурца'],
        'помидоры': ['помидор', 'помидоров', 'томат'],
        'помидор': ['помидоры', 'помидоров', 'томат'],
        'масло подсолнечное': ['масло растительное', 'подсолнечное масло'],
        'масло растительное': ['масло подсолнечное', 'подсолнечное масло']
    };
    
    if (synonyms[userNorm]) {
        for (let syn of synonyms[userNorm]) {
            if (syn === recipeNorm || recipeNorm.includes(syn) || syn.includes(recipeNorm)) return true;
        }
    }
    
    if (synonyms[recipeNorm]) {
        for (let syn of synonyms[recipeNorm]) {
            if (syn === userNorm || userNorm.includes(syn) || syn.includes(userNorm)) return true;
        }
    }
    
    return false;
}
