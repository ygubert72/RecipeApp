// Сравнение ингредиентов
export function isIngredientMatch(userIngredient, recipeIngredient) {
    if (!userIngredient || !recipeIngredient) return false;
    
    const userNorm = normalizeIngredient(userIngredient);
    const recipeNorm = normalizeIngredient(recipeIngredient);
    
    if (userNorm === recipeNorm) return true;
    if (userNorm.includes(recipeNorm) || recipeNorm.includes(userNorm)) return true;
    
    // Дополнительные синонимы (резервные)
    const extraSynonyms = {
        'яйца куриные': ['яйцо', 'яйца', 'яйцо куриное', 'куриное яйцо', 'куриные яйца'],
        'соль': ['соль поваренная', 'соль морская', 'соли'],
        'перец': ['перец черный', 'черный перец', 'перец черный молотый', 'молотый перец', 'перец душистый', 'перец красный жгучий', 'перец чили'],
        'грецкий орех': ['орех грецкий', 'грецкие орехи', 'орехи грецкие'],
        'кешью': ['орех кешью', 'кэшью'],
        'миндаль': ['орех миндаль', 'миндальный орех'],
        'фундук': ['орех фундук', 'лесной орех'],
        'подсолнечное масло': ['масло подсолнечное', 'растительное масло'],
        'оливковое масло': ['масло оливковое'],
        'куриное филе': ['филе куриное', 'куриная грудка', 'грудка куриная'],
        'помидоры': ['помидор', 'томат', 'томаты'],
        'огурцы': ['огурец'],
        'кабачки': ['кабачок', 'цукини'],
        'баклажаны': ['баклажан', 'синенькие'],
        'морковь': ['морковка'],
        'картофель': ['картошка', 'картофелина']
    };
    
    if (extraSynonyms[userNorm]) {
        for (let syn of extraSynonyms[userNorm]) {
            if (syn === recipeNorm || recipeNorm.includes(syn) || syn.includes(recipeNorm)) {
                return true;
            }
        }
    }
    
    if (extraSynonyms[recipeNorm]) {
        for (let syn of extraSynonyms[recipeNorm]) {
            if (syn === userNorm || userNorm.includes(syn) || syn.includes(userNorm)) {
                return true;
            }
        }
    }
    
    return false;
}
