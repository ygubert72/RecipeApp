export function getAIAnswer(question) {
    const q = question.toLowerCase();
    if (q.includes('заменить яйца')) return 'Яйца можно заменить: банановым пюре (1 яйцо = 1/2 банана), льняной мукой с водой, или тофу.';
    if (q.includes('заменить сливки')) return 'Сливки заменяют: кокосовое молоко, смесь молока с маслом, или густая сметана с молоком.';
    if (q.includes('сочное мясо')) return 'Маринуй в кефире/йогурте 2 часа, не пережаривай, дай отдохнуть 5-10 минут после готовки.';
    if (q.includes('хрустящая корочка')) return 'Обсуши продукты перед жаркой, используй панировку, жарь на хорошо разогретой сковороде.';
    if (q.includes('тыквенные семечки') || q.includes('семечки тыквенные')) return 'Тыквенные семечки можно добавить в салаты, супы-пюре или просто поджарить как перекус!';
    return 'Отличный вопрос! Совет: всегда пробуй блюдо в процессе готовки.';
}
